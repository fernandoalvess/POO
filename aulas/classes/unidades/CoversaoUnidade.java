package aulas.classes.unidades;

public class CoversaoUnidade {
    public static float polegadasParaCentimetro(float polegadas) {
        return polegadas * 2.54f;
    }

    public static float pesParaCentimetro(float pes) {
        return pes * 30.48f;
    }

    public static float milhasParaKm(float milhas) {
        return milhas * 1.609f;
    }
}
